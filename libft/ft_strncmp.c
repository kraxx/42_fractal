#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while ((*s1 || *s2) && n--)
		if (*s1++ != *s2++)
			return (*(t_uc *)--s1 - *(t_uc *)--s2);
	return (0);
}
